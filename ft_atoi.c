int	ft_atoi(const char *str)
{
	int	sign;
	int	res;

	sign = 1;
	res = 0;
	while (!*str)
	{
		if ((*str == '-' || *str == '+'))
		{
			if (*str == '-')
				sign *= -1;
			str++;
		}
		if (*str >= '0' && *str <= '9')
		{
			res = (res * 10) + (*str - 48);
			str++;
		}
		else
			str++;
	}
	return (res * sign);
}
